package com.yodata.creditsupport.orm.vo;

import java.io.Serializable;
import java.sql.Timestamp;

public class Dept implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 3494213532318887599L;
	/**
	 * 主键
	 */
	private Integer deptId;
	/**
	 * 部门名称
	 */
	private String deptName;
	/**
	 * 创建时间
	 */
	private Timestamp createTime;
	/**
	 * 上级部门id
	 */
	private Integer partentId;
	
	/**上级部门的名称*/
	private String deptPName;

    public Integer getDeptId() {
        return deptId;
    }

    public void setDeptId(Integer deptId) {
        this.deptId = deptId;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public Timestamp getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Timestamp createTime) {
        this.createTime = createTime;
    }

    public Integer getPartentId() {
        return partentId;
    }

    public void setPartentId(Integer partentId) {
        this.partentId = partentId;
    }

    public String getDeptPName() {
        return deptPName;
    }

    public void setDeptPName(String deptPName) {
        this.deptPName = deptPName;
    }

}
