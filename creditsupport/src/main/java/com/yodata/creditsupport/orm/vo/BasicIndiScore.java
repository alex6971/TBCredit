package com.yodata.creditsupport.orm.vo;

import java.io.Serializable;

public class BasicIndiScore implements Serializable {

    private static final long serialVersionUID = 2898252167241906727L;
    private String itemName;		//名称
	private String base;			//基本信息
	private String behavior;		//交易行为
	private String history;			//信用历史
	private String capacity;		//履约能力
	private String special;			//特殊事项调整
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getBase() {
		return base;
	}
	public void setBase(String base) {
		this.base = base;
	}
	public String getBehavior() {
		return behavior;
	}
	public void setBehavior(String behavior) {
		this.behavior = behavior;
	}
	public String getHistory() {
		return history;
	}
	public void setHistory(String history) {
		this.history = history;
	}
	public String getCapacity() {
		return capacity;
	}
	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}
	public String getSpecial() {
		return special;
	}
	public void setSpecial(String special) {
		this.special = special;
	}
}
