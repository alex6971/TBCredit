package com.yodata.creditsupport.orm.vo;

import java.util.Date;

public class SpecialInfoVo {
	private Integer id ;
	private String regId ;
	private String name	;//		被执行人姓名
	private String object;//		执行标的
	private String court	;//		执行法院
	private String status	; // 案件状态 1/2
	private String num	;//		案号
	private Date caseDate	;	//	立案时间
	private Date getinfoDate	;//  信息获取日期
	
	
	public SpecialInfoVo(){}
	
	public SpecialInfoVo(Integer id,String regId,String name, String object,String court,
			String status,String num,Date caseDate,Date getinfoDate){
		this.id = id ;
		this.regId = regId ;
		this.name = name;
		this.object = object;
		this.court = court;
		this.status = status;
		this.num = num;
		this.caseDate = caseDate;
		this.getinfoDate = getinfoDate ;
	}
	
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getRegId() {
		return regId;
	}

	public void setRegId(String regId) {
		this.regId = regId;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getObject() {
		return object;
	}
	public void setObject(String object) {
		this.object = object;
	}
	public String getCourt() {
		return court;
	}
	public void setCourt(String court) {
		this.court = court;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public Date getCaseDate() {
		return caseDate;
	}
	public void setCaseDate(Date caseDate) {
		this.caseDate = caseDate;
	}
	public Date getGetinfoDate() {
		return getinfoDate;
	}
	public void setGetinfoDate(Date getinfoDate) {
		this.getinfoDate = getinfoDate;
	}
	
	
}
